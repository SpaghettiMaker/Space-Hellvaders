Obstacle::Obstacle(Vector2 position)
{
    this->position = position;

    for (unsigned i = 0; i < grid.size(); i++)
    {
        for (unsigned j = 0; j < grid[i].size(); j++)
        {
            if (grid[i][j] == 1)
            {
                // EVERY BLOCK IS 3 PIXEL WIDE AND PIXEL TALL
                float x = (float)j * 3 + position.x;
                float y = (float)i * 3 + position.y;
                blocks.push_back(Block({x, y}));
            }
        }
    }
}

Obstacle::~Obstacle()
{
}

void Obstacle::Draw()
{
    for (Block block : blocks)
    {
        block.Draw();
    }
}
